# Define a dictionary
data = {"name": "Minte",
        "age": 20,
        "city": "Addis Ababa"}
# Get all the values from the dictionary
values = data.values()
# Convert to a list if needed
values_list = list(values)
print("Values as a list:", values_list)
# Output: Values as a list: ['Minte', 20, 'Addis Ababa']
