# Demonstrating the use of the list index() method in Python

# Define a sample list
fruits = ["apple", "banana", "cherry", "orange"]

# Find the index of the occurrence of 'banana'
index_banana = fruits.index('banana')
print(f"The occurrence of 'banana' is at index: {index_banana}")
# Output: "The occurrence of 'banana' is at index: 1"