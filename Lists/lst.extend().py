# Example of using the extend() method in Python

# Define two lists
Fruit_list1 = ["Mango", "Banana", "Apple"]
Fruit_list2 = ["Strawberry", "orange", "grape"]

# Extend list1 by adding elements of list2
Fruit_list1.extend(Fruit_list2)

# Print the updated list1
print("Extended list:", Fruit_list1)
# Output: "Extended list: ['Mango', 'Banana', 'Apple', 'Strawberry', 'orange', 'grape']"